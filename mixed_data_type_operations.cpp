/*
Write a C++ program that displays mixed data types and arithmetic operations.
*/

#include <iostream>

using namespace std;

int main() {
    int a = 5;
    int b = 7;
    float c = 3.7;
    float d = 8.0;

    cout << a + b << endl;
    cout << c + d << endl;
    cout << a + d << endl;
    cout << a - b << endl;
    cout << c - d << endl;
    cout << a - d << endl;
    cout << a * b << endl;
    cout << c * d << endl;
    cout << a * d << endl;
    cout << a / b << endl;
    cout << c / d << endl;
    cout << a / d << endl;
    return 0;

}
